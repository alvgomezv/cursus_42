#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*current;

	current = *lst;
	while (current->next)
		current = current->next;
	current->next = new;
	new->next = NULL;
}

/*
Adds the node ’new’ at the end of the list.
*/